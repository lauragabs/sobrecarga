class Adicao1 {

    public int addInt(int x, int y) {
        return x + y;
    }

    public float addFloat(float x, float y) {
        return x + y;
    }

    public String exibeInt(int x, int y, int z) {
        return "Soma dos inteiros: " + (x + y + z);
    }

    public String exibeFloat(float x, float y, float z) {
        return "Soma dos floats: " + (x + y + z);
    }
}