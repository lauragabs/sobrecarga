class Adicao2 {

    public int add(int x, int y) {
        return x + y;
    }

    public float add(float x, float y) {
        return x + y;
    }

    public String exibe(int x, int y, int z) {
        return "Soma dos inteiros: " + (x + y + z);
    }

    public String exibe(float x, float y, float z) {
        return "Soma dos floats: " + (x + y + z);
    }
}